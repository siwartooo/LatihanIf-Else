let hari = 3;

if (hari === 1) {
  console.log("Senin");
} else if (hari === 2) {
  console.log("Selasa");
} else if (hari === 3) {
  console.log("Rabu");
} else if (hari === 4) {
  console.log("Kamis");
} else if (hari === 5) {
  console.log("Jumat");
} else if (hari === 6) {
  console.log("Sabtu");
} else if (hari === 7) {
  console.log("Minggu");
} else {
  console.log("Input tidak valid");
}
